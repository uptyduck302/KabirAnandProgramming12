package com.anand.module1_7;

import java.math.BigInteger;

// Class providing utility methods for BigInteger
public class BigInt {

    /**
     * Multiplies two big integers together.
     * @param a a BigInteger
     * @param b a BigInteger
     * @return a * b
     */
    public static BigInteger multiply(BigInteger a, BigInteger b) {
        return a.multiply(b);
    }
}
