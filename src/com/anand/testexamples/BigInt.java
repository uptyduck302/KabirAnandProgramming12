package com.anand.testexamples;

import java.math.BigInteger;

public class BigInt {

    /**
     * Multiplies two big integers together.
     * @param a a BigInteger
     * @param b a BigInteger
     * @return a * b
     */
    public static BigInteger multiply(BigInteger a, BigInteger b){
        return a.multiply(b);
    }
}